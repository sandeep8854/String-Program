
public class String_4_GetCharacter {

	public static void main(String[] args)
	{
		//create String
		String str1="Hello World";
		System.out.println(str1);
		
		//return character 3
		System.out.println("The Character at 3 " +str1.charAt(3));
		
		System.out.println("The Character at 7 " + str1.charAt(6));
	}
}
